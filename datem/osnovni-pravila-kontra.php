<p>Печели играча, който пръв вземе всичките си пулове. Играта е двойна (марс, гемън), ако противничката Ви не е взела нито един пул и тройна (бекгемън, още една разлика с таблата на Балканите), ако има пулове във вътрешното Ви табло или бара.</p>
<h3 id="notaciya" >Нотация. Запис на партиите по спортна табла</h3>
