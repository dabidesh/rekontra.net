<p>Пример: ако началното хвърляне и следващото са 31 и играчите правилно хванат златните капии, хода се пише 31: 8/5 6/5 и за двамата.</p>
<p>Когато се удря гол пул се отбелязва със звездичка — 65: 13/7* 6/1. Когато се местят повече от 1 пул в скоби се пише броят — 55: 13/3(2). Междинната капия не се пише, освен в случаите когато се удря гол пул — 22: 24/20*/18*/16. Когато се влиза от бара се пише „бар“ (bar), а когато се излиза — „вън“ (off).</p>
<h3 id="kontra" >Правило за контра и реконтра: основна разлика с обикновената табла</h3>
<p>
В състезателната табла се въвежда правилото за контриране. Така играта става много по-интересна и вълнуваща, но и много по трудна. Може да се контрира (реконтрира) неограничен брой пъти — мач до 25 точки може да завърши за една партия!
</p>


<ol>
<li>В началото и двамата играчи могат да контрират: контриращото кубче е по средата и е установено на 1 (но пише 64).
</li>
<li>Контрирането става преди да се хвърлят заровете. Трябва да се каже „контра“ (double [дабъл] = удвояване).
</li>
<li>Играчът получил контра има два избора: да се предаде (pass [пас] = прекратявам, отказвам се, пас) или да приеме (take [тейк] = приемам).
</li>
<li>Ако каже пас, т.е. се предаде партията се прекратява и играчът обявил контра печели точките, които показва контриращото кубче.
</li>
<li>Ако приеме, партията продължава като показанието на контриращото кубче се удвоява (при 1-вата контра става 2, после 4 и т.н.).
</li>
<li>Ако е дадена контра само приелият я играч може да контрира (владее контриращото кубче). Контрите след 1-вата се наричат реконтра (redouble [ридабъл] = повторно удвояване).
</li>
<li>Ако са достигнати точките за победа в мача не е разрешено контрирането/реконтрирането (няма и смисъл).
</li>
<li>Конвенция за автоматична контра: При хвърлянето на еднакви зарове в началото на играта, контриращото кубче се установява на 2 като остава в средата. Това се прави само веднъж за всяка партия. Това правило не е много популярно. Ползва се в игрите за пари. 
</li>
<li>Правило на Крауфорд (Crawford): ако някой от играчите достигне до една точка до победата, контриращото кубче се замразява, т.е. забранено е да се ползва, но само за една партия.
</li>
<li>Правило на Джакоби (Jacoby, Джейкъби): ако не е дадена контра марсовете и <a href="?s=rechnik-termini-i-zhargon#bekgemyn" >бекгемъните (вж. т.2)</a> не се зачитат. Това правило се използва в играта за пари.
</li>
<li>След края на партията по спортна табла, спечелилият играч си пише точки колкото показва контриращото кубче. Ако е марс точките се умножават по 2. Ако е бекгемън се умножават по 3.
</li>
</ol>
<h3 id="turnirni" >Турнирни правила в спортната табла</h3>
<p>Турнирните правила в спортната табла се различават от общоприетите по кръчмите на Балканите. Затова, ако ще участвате в турнири на живо трябва да се запознаете с тях.</p>
<ul>
<li>Пуловете се местят само с една ръка.</li>
<li>Правилото пипнато—местено не важи. Разрешено е връщането на хода.</li>
